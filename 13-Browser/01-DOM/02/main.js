// จงเขียนคำสั่งเพื่อ access \<h2> โดยใช้วิธีต่อไปนี้
// - querySelector
// - querySelectorAll
// - getElementsByClassName
// - getElementsByTagName

const h2_a = document.querySelector("h2");
const h2_b = document.querySelectorAll("h2");
const h2_c = document.getElementsByClassName("title-1");
const h2_d = document.getElementsByTagName("h2");
console.log(h2_a)
console.log(h2_b)
console.log(h2_c)
console.log(h2_d)